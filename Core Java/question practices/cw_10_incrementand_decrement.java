public class cw_10_incrementand_decrement {
    public static void main(String[] args) {
        
        // increment and decrement operators
        int i = 56;
        // int b = i++;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
    }
    
}
